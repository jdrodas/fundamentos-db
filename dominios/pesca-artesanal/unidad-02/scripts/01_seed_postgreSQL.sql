-- ¿Cuántas capturas y kg totales hay por cuenca?
select cu.nombre as cuenca,
       count(*)              as total_capturas,
       sum(c.cantidad_kg)    as total_kg
from capturas c
join municipios mun on mun.id = c.municipio_id
join cuencas    cu  on cu.id     = mun.cuenca_id
group by cu.nombre
order by total_kg desc;

-- ¿cuántas capturas hay por año y mes?
select
    extract(year from fecha_hora) as año,
    extract(month from fecha_hora) as mes,
       count(*)                        as total_capturas,
       sum(cantidad_kg)                as total_kg
from capturas
group by año, mes
order by año, mes;

-- Top 5 especies por kg total capturado
select e.nombre_comun,
       round(sum(c.cantidad_kg), 2) as total_kg
from capturas c
join especies e on e.id = c.especie_id
group by e.nombre_comun
order by total_kg desc
limit 5;